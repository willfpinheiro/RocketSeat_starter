// Para fazer a exportação e importação de funções entre arquivos JS é extritamente necessario ser o mesmo nome
export default function somaWebPack(a, b) {
	return a + b;
}

export function subWebPack(a, b) {
	return a - b;
}

// export defult, é quando um arquivo js conterm somente uma função ou é declarada como default

export function multWebPack(a, b) {
	return a * b;
}
